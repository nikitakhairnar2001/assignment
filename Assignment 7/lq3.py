dic =   [{'make': 'Nokia', 'model': 216, 'color': 'Black'}, {'make': 'Mi Max', 'model': '2', 'color': 'Gold'}, {'make': 'Samsung', 'model': 7, 'color': 'Blue'}]
print("Original list of dictionaries :",dic)
s = lambda item: item['color']  
dic.sort(key = s)
print("Sorting the List of dictionaries :",dic)

